<p>
&#9851;Bảo vệ môi trường là vấn đề cấp thiết và đang ngày càng được chú trọng khi môi trường sống trên trái đất 
đang ngày càng xuống cấp nghiêm trọng. <br>

&#9851;Thời gian gần đây đã có nhiều hoạt động, phong trào thiết thực được hưởng ứng nhiệt liệt 
để bảo vệ môi trường như thử thách dọn rác gây sốt trên mạng xã hội, các siêu thị, 
cửa hàng sử dụng lá chuối gói thực phẩm thay túi nylon, 
các cuộc cách mạng sáng tạo vật liệu thân thiện với môi trường,...<br>

&#9851;Tuy nhiên để có thể bảo vệ môi trường một cách toàn diện, 
bền vững thì cần thay đổi ngay từ thói quen sinh hoạt, cuộc sống hàng ngày, 
hạn chế tối đa việc xả rác bừa bãi, hạn chế sử dụng những sản phẩm dùng một lần 
thay vào đó là những sản phẩm thân thiện với môi trường. 
